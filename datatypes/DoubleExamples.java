public class DoubleExamples {
   
        public static void main(String[] args) {
        
            double distanceKm = 42.195;
    
            double productPrice = 99.99;
    
            double weightKg = 15.5;

            double buildingHeight = 150.75;

            double temperatureCelsius = 25.5;

            double vehicleSpeedKph = 120.5;

            double circleArea = 314.159;

            double rainfallMm = 50.2;
 
            double latitude = 40.7128;
   
            double longitude = -74.0060;
    
            System.out.println("Distance in km: " + distanceKm);
            System.out.println("Product price in dollars: " + productPrice);
            System.out.println("Weight in kg: " + weightKg);
            System.out.println("Building height in meters: " + buildingHeight);
            System.out.println("Temperature in Celsius: " + temperatureCelsius);
            System.out.println("Vehicle speed in km/h: " + vehicleSpeedKph);
            System.out.println("Circle area in sq. meters: " + circleArea);
            System.out.println("Rainfall in mm: " + rainfallMm);
            System.out.println("Latitude: " + latitude);
            System.out.println("Longitude: " + longitude);
        }
    }
    

