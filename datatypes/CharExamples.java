public class CharExamples {
        public static void main(String[] args) {

            char grade = 'A';
  
            char dollarSymbol = '$';
    
            char gender = 'M';
    
            char passwordChar = '#';

            char digit = '5';

            char multiplicationSymbol='*';

            char greaterthanSymbol='>';

            char questionmark='?';

            char lessthanSymbol='<';


    
            System.out.println("Grade: " + grade);
            System.out.println("Dollar Symbol: " + dollarSymbol);
            System.out.println("greaterthanSymbol: " + greaterthanSymbol);
            System.out.println("Gender: " + gender);
            System.out.println("Password Character: " + passwordChar);
            System.out.println(" multiplicationSymbol: " + multiplicationSymbol);
            System.out.println("Digit: " + digit);
            System.out.println(" questionmark : " + questionmark);
            System.out.println("lessthanSymbol: " + lessthanSymbol);
        }
    }
    

