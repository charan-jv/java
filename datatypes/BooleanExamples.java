public class BooleanExamples {
        public static void main(String[] args) {
         
            boolean isLightOn = true;

            boolean isDoorOpen = false;

            boolean hasAdminRights = true;

            boolean isReadOnly = false;
   
            boolean isConnected = true;
 
            boolean isLoggedIn = false;

            boolean isSubscriptionActive = true;
  
            boolean isEngineRunning = false;
  
            boolean isAboveFreezing = true;
    
            boolean isFullyCharged = false;

            System.out.println("Is the light on? " + isLightOn);
            System.out.println("Is the door open? " + isDoorOpen);
            System.out.println("Does the user have admin rights? " + hasAdminRights);
            System.out.println("Is the file read-only? " + isReadOnly);
            System.out.println("Is the internet connected? " + isConnected);
            System.out.println("Is the user logged in? " + isLoggedIn);
            System.out.println("Is the subscription active? " + isSubscriptionActive);
            System.out.println("Is the car engine running? " + isEngineRunning);
            System.out.println("Is the temperature above freezing? " + isAboveFreezing);
            System.out.println("Is the battery fully charged? " + isFullyCharged);
        }
    }
    
    

