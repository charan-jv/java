public class TrafficSignal {

    public static void main(String[] args){
        System.out.println("Traffic Signal rule");
        red();
        green();
    }

    public static void red(){
        System.out.println("vehicals should Stop when signal is red");
    }
    static void green(){
        System.out.println("vehicals can Go when signal is green ");
    }

    
}
